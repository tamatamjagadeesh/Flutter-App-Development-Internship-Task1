void main() {
  // Conditional Statements

  // If
  var num = 0;
  if (num == 0) {
    print(num);
  }

  // If-Else
  var num2 = 11;
  if (num2 % 2 == 0) {
    print("This is Even Number:$num2");
  } else {
    print("This is Odd Number:$num2");
  }

  // If Else If
  var int = 22;
  if (int == 2) {
    print(int);
  } else if (int == 23) {
    print(int);
  } else {
    print("Error");
  }
}
