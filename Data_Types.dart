void main() {
  //Data types in Dart
  //Numbers,Strings,Booleans,Lists,Maps,Sets,Dynamic

  int age = 25; //integer
  double height = 5.6; //double
  String name = "Alice"; //string
  bool isStudent = true; //boolean
  List<String> hobbies = ["Reading", "Traveling", "Cooking"]; //list
  Map<String, int> scores = {"Math": 90, "Science": 85}; //map
  Set<String> uniqueNames = {"Alice", "Bob", "Charlie"}; //set
  dynamic variable = "Hello"; //dynamic variable can hold any type of value

  print("String: $name");
  print("Integer: $age");
  print("Double: $height");
  print("Boolean: $isStudent");
  print("List: $hobbies");
  print("Map: $scores");
  print("Set: $uniqueNames");
  print("Dynamic: $variable");
}
