void main() {
  // Type Conversion

  // String to Int
  var str = "123";
  var num = int.parse(str);
  print(num);

  // String to Double
  var str2 = "123.45";
  var num2 = double.parse(str2);
  print(num2);

  // Int to String
  var num3 = 123;
  var str3 = num3.toString();
  print(str3);

  // Double to String
  var num4 = 123.45;
  var str4 = num4.toString();
  print(str4);
}
